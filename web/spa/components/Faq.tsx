export default function Faq({ lang }: { lang: Lang }) {
  // Multiple items can stay open; the first one is open by default.
  const [open, setOpen] = useState<Record<string, boolean>>({ '01': true });
  const toggle = (qn: string) => setOpen((s) => ({ ...s, [qn]: !s[qn] }));

  return (
    <div className="faq-list">
      {groups(lang).map((g) => (
        <section className="faq-group" key={g.n}>
          <div className="faq-group-h">
            <span className="faq-group-n">{g.n}</span> {g.title}
          </div>
          {g.items.map((it) => {
            const isOpen = !!open[it.qn];
            return (
              <div className={`faq-item${isOpen ? ' open' : ''}`} key={it.qn}>
                <button
                  type="button"
                  className="faq-q"
                  aria-expanded={isOpen}
                  onClick={() => toggle(it.qn)}
                >
                  <span className="faq-qn">{it.qn}</span>
                  <span className="faq-qt">{it.q}</span>
                  <span className="faq-ind" aria-hidden="true" />
                </button>
                <div className="faq-a">
                  <div className="faq-a-inner">
                    <p>{it.a}</p>
                  </div>
                </div>
              </div>
            );
          })}
        </section>
      ))}
    </div>
  );
}
